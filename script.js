function conferirProfissao() {
    var respProfissao = document.querySelector('input#respostaProfissao')
    var resP = document.querySelector('div#resultadoProfissao')
    var resultadoP = String(respProfissao.value)
    
    if (resultadoP === 'Profissão') {
        resP.innerHTML = ` Parabéns, está correto!` 
    } else{
        resP.innerHTML = `Resposta errada, tente outra vez!`
    }
    } 

